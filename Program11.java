class CompareTwoNumbers{
    public static void main(String args[]){
        int num1 = 3;
        int num2 = 5;

        if(num1 > num2){
            System.out.println(num1 + " greater than " + num2);
        }
        else if(num1 < num2){
            System.out.println(num1 + " less than " + num2);
        }
        else{
            System.out.println("equal numbers");
        }
    }
}